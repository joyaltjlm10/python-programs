m=float(input("Enter Your Marks : "))
if (m>=90) :
    print ("A+")
elif (m>=80) :
    print ("A")
elif (m>=70) :
    print ("B+")
elif (m>=60) :
    print ("B")
elif (m>=50) :
    print ("C+")
elif (m>=40) :
    print ("C")
elif (m>=30) :
    print ("D")
else :
    print ("Failed !!!")